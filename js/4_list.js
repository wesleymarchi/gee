/* Acessando e alterando itens em uma lista. */

var colors = ['red',
    'green',
    'blue'];

print("Lista original: ", colors);
print("Exibindo cada item da lista de acordo com suas posições: \n", colors[0], colors[1], colors[2]);

// Alterando o segundo item da lista.
colors[1] = 'white';

print("Segundo item da lista: ", colors[1]);
print("Lista completa: ", colors);